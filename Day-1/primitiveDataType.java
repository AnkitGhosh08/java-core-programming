
public class primitiveDataType {
        static int a ;
        //int store whole number =(-2,147,483,648) to (2,147,483,648)
        static float b;
        //float is sufficient to storing 6 to 7 digit decimal.
        static double c;
        //double is sufficient to storing (15 decimal digit)
        static short d;
        //short value=(-32726) to (32767).
        static boolean e;
        //boolean store true or false value.
        static char f;
        // char store a single character/letter.
        static byte g;
        //byte value = (-127) to (128).
    public static void main(String[] args) {
        {
            System.out.println("Default values of primitive data type is.");
        }
        {
            System.out.println("int = " + a);
        }
        {
            System.out.println("float = " +b);
        }
        {
            System.out.println("double = " + c);
        }
        {
            System.out.println("short = " + d);
        }
        {
            System.out.println("boolean = " + e);
        }
        {
            System.out.println("char = " + f);
        }
        {
            System.out.println("byte = " + g);
        }

    }
}